from transformers import AutoTokenizer, AutoModelForCausalLM, pipeline
from langchain_huggingface import HuggingFacePipeline

# Load model
model_name = "TinyLlama/TinyLlama-1.1B-Chat-v1.0"

tokenizer = AutoTokenizer.from_pretrained(model_name)
model = AutoModelForCausalLM.from_pretrained(
    model_name,
    device_map="auto",
    torch_dtype="auto",
    trust_remote_code=True
)

# Create pipeline with clean generation settings
pipe = pipeline(
    "text-generation",
    model=model,
    tokenizer=tokenizer,
    max_new_tokens=64,          # Keep responses short
    temperature=0.7,
    top_k=50,
    do_sample=True,
    pad_token_id=tokenizer.eos_token_id
)

# Wrap with LangChain
llm = HuggingFacePipeline(pipeline=pipe)

print("âœ… Model loaded!")

"""### ğŸ§© Add Memory: Clean Conversation History"""

from langchain.memory import ConversationBufferMemory
from langchain.chains import LLMChain
from langchain.prompts import PromptTemplate

# Simple prompt â€” no special tokens in template
template = """Past conversation:
{history}

Human: {input}
AI:"""

prompt = PromptTemplate(
    input_variables=["history", "input"],
    template=template
)

# Memory stores only clean input/output pairs
memory = ConversationBufferMemory(
    input_key="input",
    memory_key="history",
    format_messages=False  # Critical: avoids storing full prompt
)

# Create chain
conversation = LLMChain(
    llm=llm,
    prompt=prompt,
    memory=memory
)

"""### ğŸ’¬ Clean Chat Demo: Watch the AI Remember"""

def chat(message):
    response = conversation.invoke({"input": message})
    return response['text'].strip()

print("ğŸ—¨ï¸  CHAT DEMO: AI with Memory\n")
